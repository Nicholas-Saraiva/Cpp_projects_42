enum msgCode {
	DEBUG,
	INFO,
	WARNING,
	ERROR,
	UNKNOWN
};

msgCode	hashString(const std::string level)
{
	if (level == "DEBUG") return DEBUG;
	if (level == "INFO") return INFO;
	if (level == "WARNING") return WARNING;
	if (level == "ERROR") return ERROR;
	return UNKNOWN;
}

